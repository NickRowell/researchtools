// Represents coordIndex
class Quadruple
{
    float w;
    float x;
    float y;
    float z;

    public Quadruple(final float _w, final float _x, final float _y, final float _z)
    {
        w = _w;
        x = _x;
        y = _y;
        z = _z;
    }
    
}
