package util;

/**
 * Static fields for different unusual characters.
 *
 * @author nrowell
 * @version $Id$
 */
public class CharUtil {
	
	/**
	 * The symbol for Solar quantities ('O-dot').
	 */
	public static final String solar = "\u2299";
	
	/**
	 * Variation on the greek lower case omega that is commonly used to indicate parallax.
	 */
	public static final String parallax = "\u03D6";
	
	/**
	 * Lower case epsilon, often used to indicate error.
	 */
	public static final String greek_e = "\u03B5";
	
	/**
	 * Greek lower case sigma.
	 */
	public static final String greek_s = "\u03C3";
	
	/**
	 * Circular buller point.
	 */
	public static final String bullet = "\u2022";
	
	/**
	 * Triangular bullet point.
	 */
	public static final String bullet_triangle = "\u2023";
	
}